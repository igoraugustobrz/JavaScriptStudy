/* OPERADORES DE ATRIBUIÇÃO */
let numero = 20;

numero += 10; // numero = numero + 10;
numero -= 10; // numero = numero - 10;
numero *= 10; // numero = numero * 10;
numero /= 10; // numero = numero / 10;

